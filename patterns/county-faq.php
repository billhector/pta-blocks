    <!-- /wp:heading -->

    <!-- wp:details -->
    <details class="wp-block-details"><summary>Who can file a property tax appeal?</summary>
    <!-- wp:paragraph -->
    <p>Any property owner who believes the assessed value of their property is too high. You typically need to be the owner of record on the date the appeal is filed.</p>
    <!-- /wp:paragraph -->
    </details>
    <!-- /wp:details -->

    <!-- wp:details -->
    <details class="wp-block-details"><summary>What's the deadline to file?</summary>
    <!-- wp:paragraph -->
    <p>Deadlines vary by county and assessment cycle. Use the municipality lookup above to find your specific deadline. Missing the deadline means waiting another year, so file early.</p>
    <!-- /wp:paragraph -->
    </details>
    <!-- /wp:details -->

    <!-- wp:details -->
    <details class="wp-block-details"><summary>Do I need a lawyer?</summary>
    <!-- wp:paragraph -->
    <p>No. The appeal process is designed for property owners to file themselves. Our guide walks you through every step, including the evidence to gather and how to present it.</p>
    <!-- /wp:paragraph -->
    </details>
    <!-- /wp:details -->

    <!-- wp:details -->
    <details class="wp-block-details"><summary>What happens after I file?</summary>
    <!-- wp:paragraph -->
    <p>The appeal board reviews your evidence, may schedule a hearing, and issues a decision. If your appeal is granted, your assessed value is reduced and your tax bill is recalculated. The full timeline is in the guide.</p>
    <!-- /wp:paragraph -->
    </details>
    <!-- /wp:details -->

    <!-- wp:details -->
    <details class="wp-block-details"><summary>What if my appeal is denied?</summary>
    <!-- wp:paragraph -->
    <p>You typically have the right to escalate to a state-level appeals board or court. The guide covers the escalation steps specific to your county.</p>
    <!-- /wp:paragraph -->
    </details>
